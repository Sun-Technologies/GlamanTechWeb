      <h1 id="side-nav" class="page-title navbar-example">Interview Tips</h1>
       <hr/>
       <p>Read on the tips for a successful interview.</p>
       <h3 style="text-align: center; padding-top: .5%;background-color:#DCDCDC; padding-bottom: 1%;border-radius: 5px 5px 5px 5px;">Before Interview</h3>
       <h4>Research the company</h4>
       <p>Research everything about the place via company website. Know about the organization work culture to know if it is the right place to work at.</p>
       <h4>Know the position description</h4>
       <p>Understand the job description, your roles and responsibilities fully, and prepare yourself. Show your interest in the position throughout your selection process.</p>
       <h4>Understand job Requirements</h4>
       <p>Know the job requirements and prepare a list of your strengths, skills and experiences which interviewer can link to the position.</p>
       <h4>Prepare question</h4>
       <p>Be prepared with questions in advance to ask the interviewer like if you are uncertain about roles and responsibilities, just ask for clarification.</p>
       <h3 style="text-align: center; padding-top: .5%; background-color:#DCDCDC; padding-bottom: 1%;border-radius: 5px 5px 5px 5px;">Day Of Interview</h3>
       <h4>Dress appropriately</h4>
       <p>Appearance counts on the day of interview. Understand the work culture of the company and what is their dress code, then dress yourself accordingly.</p>
       <h4>Arrive early</h4>
       <p>The first chance to show your attitude and commitment, is to arrive few minutes early for the personal interview.</p>
       <h4>Documents</h4>
       <p>Be prepared with all your documents and make sure to get copies of the same.</p>
       <h3 style="text-align: center; padding-top: .5%; background-color:#DCDCDC;padding-bottom: 1%;border-radius: 5px 5px 5px 5px;">Interview</h3>
       <h4>Body language</h4>
       <p>Make your body language positive and confident. Always smile, speak in clear voice, make eye contact and greet the interviewer with a firm handshake. Sit forward in your chair and show your interest towards the organization.</p>
       <h4>Answer properly</h4>
       <p>Take 1-2 minutes to understand the question and answer and support them with appropriate examples if needed.</p>
       <h4>Stay calm</h4>
       <p>Stay calm and do not waffle. Always talk positive about colleagues and boss. Tell about all the positive experiences you had in your previous organization.</p>
       <h4>Ask questions</h4>
       <p>Always be prepared with questions for your interviewer. Ask about the culture and objective of the company. Ask about interviewer experiences in the company.</p>
       <h3 style="text-align: center; padding-top: .5%; background-color:#DCDCDC;padding-bottom: 1%;border-radius: 5px 5px 5px 5px;">After Interview</h4>
       <h4>Make sure you have contact information</h4>
       <p>Get the contact number or email id of the interviewer or HR team for future contact and to send thank you note. Ask for the business card.</p>
       <h4>Discuss next steps</h4>
       <p>Before leaving enquire about the next round. If you forget to ask about the next step, just ask in your follow-up email.</p>
       <h4>Thank everyone</h4>
       <p>Make sure you thanking the interviewer before leaving with a smile and firm handshake.</p>
       <h4>Follow up Letter</h4>
       <p>Send a follow up letter to the interviewer. If interviewer asked you then feel free to call then call them to know about the next round. If you are rejected, accept it with grace.</p>
       <h4 style="padding-top: .5%;">Not to do:</h4>
      <ul class="list-style-none">
        <li>1.  Dress unprofessionally</li>
        <li>2.  Rambling on and on</li>
        <li>3.  Bad body language.</li>
        <li>4.  Negative talks/Criticizing last employer </li>
        <li>5.  Arriving late</li>
        <li>6.  Lack of interest</li>
        <li>7. Making excuses</li>
      </ul>
